package com.barmej.apod.APOD;

public class APODInformation {
    private String dateInformation;
    private String urlInformation;
    private String hdUrlInformation;
    private String media_typeInformation;
    private String service_versionInformation;
    private String titleInformation;
    private String descriptionInformation;


    public String getDateInformation() {
        return dateInformation;
    }

    public void setDateInformation(String dateInformation) {
        this.dateInformation = dateInformation;
    }

    public String getUrlInformation() {
        return urlInformation;
    }

    public void setUrlInformation(String urlInformation) {
        this.urlInformation = urlInformation;
    }

    public String getHdUrlInformation() {
        return hdUrlInformation;
    }

    public void setHdUrlInformation(String hdUrlInformation) {
        this.hdUrlInformation = hdUrlInformation;
    }

    public String getMedia_typeInformation() {
        return media_typeInformation;
    }

    public void setMedia_typeInformation(String media_typeInformation) {
        this.media_typeInformation = media_typeInformation;
    }

    public String getService_versionInformation() {
        return service_versionInformation;
    }

    public void setService_versionInformation(String service_versionInformation) {
        this.service_versionInformation = service_versionInformation;
    }

    public String getTitleInformation() {
        return titleInformation;
    }

    public void setTitleInformation(String titleInformation) {
        this.titleInformation = titleInformation;
    }

    public String getDescriptionInformation() {
        return descriptionInformation;
    }

    public void setDescriptionInformation(String descriptionInformation) {
        this.descriptionInformation = descriptionInformation;
    }
}
